  static List<Country> search(String query) {
    if (query.isEmpty) return Country.values;

    final lowerQuery = query.toLowerCase();
    return Country.values.where((country) {
      final name = country.name.toLowerCase();
      final nameEn = country.nameEn.toLowerCase();
      return name.contains(lowerQuery) || nameEn.contains(lowerQuery);
    }).toList();
  }
